            <h3 class="home-page">Overview</h3>
				<p>Invoice Factoring is an accepted form of working capital management solution. It does so by converting your unpaid invoices to cash. Invoice Factoring allows companies to receive cash instantly, customers to live by friendly payment terms and investors (the factor) to earn some profits in terms of fee. </p>
                <h3 class="home-page">What is InFactor?</h3>
                <p>InFactor (http://InFactor.io/)  is the Invoice Factoring/Discounting solution from XinFin. Built on a global framework, XDC-I11 messaging and transaction ISO20022 standard using XinFin Hybrid Blockchain technology, InFactor is a platform for factoring Trade Instruments, mainly invoices.</p>
				<p>InFactor captures the existing data sets of the domestic invoice factoring markets from regulated institutions and relays them into the global order book market based on ISO 20022 standard – thus making it accessible to international factors using a fiat based cryptocurrency. The fiat liquidity relay can be utilized to get wider and cheaper access to capital from entities with international float. Built in XDC Wallet (https://www.xdcwallet.com/) and smart contracts make invoice factoring easier, transparent and secure.</p>
				<h3 class="home-page">Supplier/Seller</h3>
				<p>Supplier/seller will be the company who puts their invoice for factoring/discounting so that they can speed up incoming cash flow by receiving timely payments.</p>
                <h3 class="home-page">Buyer</h3>
                <p>Buyer is the supplier’s customer/client who owes money to supplier for the purchases made against which supplier generates the invoice.</p>
                <h3 class="home-page">Financier</h3>
                <p>Financier is the factoring individual or company that buys the invoices at a percentage of their total value and it is their responsibility to collect the invoice payments from suppliers’ customers.</p>
                <p>Consider a Small and Medium sized Enterprise (SME) that has been addressing cash flow issues due to rigid payment terms of 30 to 120 days from its customers. SME puts a request for factoring/discounting its invoices on InFactor platform. SME here becomes the Seller/Supplier, SME’s client/customer becomes Buyer and the factoring individual or company that pays money (minus their fee after risk assessment) to the supplier becomes the Financier.</p>
                <h3 class="home-page"> What problems is InFactor solving?</h3>
                <p>InFactor is going to implement:</p>
                <div class ="home-page">
                                            <li><b>Fraud Prevention:</b>Invoices can be fake and stretched. Sometimes, the same invoice is sent to two or more invoice factoring companies (also called as double invoicing). By putting invoice factoring on blockchain, InFactor will store the entire transaction data and will assign a unique fingerprint to every invoice. InFactor is a blockchain solution that agrees with the common standard for factoring invoices and share their data to protect them from double invoicing. In a nutshell, InFactor will automatize the verification process against double invoicing leading to fraud prevention.</li>
                                            <li><b>Standardization:</b>Centralized banking systems disable smaller companies or companies with poor credit or with no financial track record from availing factoring. Most of the times, these smaller segments are turned down by banks because of the apparent level of risk involved. InFactor will be putting all the companies’ track record onto blockchain injecting transparency and standardization in the system. With ISO20022 standard in place, InFactor will be capturing the existing data sets from banks and institutions and then making it accessible to global factors. The ISO20022 messaging protocol will allow suppliers, financiers and buyers to interact with each other on an open platform and get access to the required data leading to standardization.</li>
                                            <li><b>Interoperability:</b>Interoperability of systems is a major problem when it comes to financing. InFactor platform is designed to be interoperable with ERP systems and SWIFT banking systems so that data can be fetched automatically onto blockchain for factoring. InFactor will also be interoperable with all compliance systems of buyers/vendors/suppliers making data sharing easier between all participants. Built on XinFin protocol, InFactor will operate smoothly with centralized switching and different kind of data making it interoperable with not only existing legacy systems but the blockchain based platforms too.</li>
                                        
                </div>
                <br>
                <h3 class="home-page">Benefits of InFactor platform</h3>
                <div class ="home-page">
                                            <li> An instant access to cash flow and liquidity for small businesses</li>
                                            <li>Reduces administrative costs and time for suppliers as its factor’s responsibility to collect debt.</li>
                                            <li>Factoring is less expensive than turning to bank financing and equity investors that adds up to a company’s debts or tie up collateral.</li>
                                            <li> Since factor assesses risk before submitting the request for factoring, supplier is assured of secure payments.</li>
                                            <li>Paperless, blockchain-based factoring makes process transparent, inexpensive and real-time.</li>
                                       
                </div>
